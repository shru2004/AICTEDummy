package com.example.helloandroid.pojo;

public class YearData {

    long year2014;
    long year2015;
    long year2016;

    public YearData(long year2014, long year2015, long year2016) {
        this.year2014 = year2014;
        this.year2015 = year2015;
        this.year2016 = year2016;
    }

    public YearData() {
    }

    public long getYear2014() {
        return year2014;
    }

    public void setYear2014(long year2014) {
        this.year2014 = year2014;
    }

    public long getYear2015() {
        return year2015;
    }

    public void setYear2015(long year2015) {
        this.year2015 = year2015;
    }

    public long getYear2016() {
        return year2016;
    }

    public void setYear2016(long year2016) {
        this.year2016 = year2016;
    }
}
